public class CTIMethodNotImplementedException extends RuntimeException {

	private static final long serialVersionUID = -5951756260187280650L;

	public CTIMethodNotImplementedException() {
		super();
	}

	public CTIMethodNotImplementedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CTIMethodNotImplementedException(String arg0) {
		super(arg0);
	}

	public CTIMethodNotImplementedException(Throwable arg0) {
		super(arg0);
	}

	
	
	
}
