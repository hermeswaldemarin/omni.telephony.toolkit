package br.com.omniplusoft.gateway.domain.ctiplatform.exceptions;

public class DispatchExecutionException extends RuntimeException {

	private static final long serialVersionUID = -5951756260187280650L;

	public DispatchExecutionException() {
		super();
	}

	public DispatchExecutionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DispatchExecutionException(String arg0) {
		super(arg0);
	}

	public DispatchExecutionException(Throwable arg0) {
		super(arg0);
	}

	
	
	
}
